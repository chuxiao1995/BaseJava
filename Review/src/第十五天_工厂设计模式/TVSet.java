package 第十五天_工厂设计模式;
/**
 * 
 * 工厂模式
 */
public class TVSet {
	private String Brand;
	private String Name;
	private float Price;

	public String getName() {
		return Name;
	}

	public void setName(String name) {
		Name = name;
	}

	public float getPrice() {
		return Price;
	}

	public void setPrice(float price) {
		Price = price;
	}

	public String getBrand() {
		return Brand;
	}

	public void setBrand(String brand) {
		Brand = brand;
	}
	

}
